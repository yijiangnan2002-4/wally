#ifndef _MUX_PSEUDO_H_
#define _MUX_PSEUDO_H_

/** @brief PSEUDO event type. */
typedef enum {
    PSEUDO_EVENT_READY_TO_READ = 1,          /**<  event of  ready to read*/
    PSEUDO_EVENT_READY_TO_WRITE,             /**<  event of  ready to write*/
    PSEUDO_EVENT_READY_TRANSMISSION_DONE,    /**<  event of  transmission done*/
} pseudo_callback_event_t;


/** @brief
 * This structure defines the data structure for using #SERIAL_PORT_CMD_READ_DATA to
 * read data from the serial port.
 */
typedef struct {
    uint8_t *buffer;                 /**< The buffer to store the received data. */
    uint32_t size;                   /**< Size of the data to read. */
    uint32_t ret_size;               /**< The actual size of data read from the pseudo port. */
} pseudo_port_read_data_t;

void mux_pseudo_callback(pseudo_callback_event_t event, void *user_data);

#endif
